#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	sign;

	sign = 1;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		sign = -1;
	}
	if (n / 10 != 0)
		ft_putnbr_fd(n / 10 * sign, fd);
	ft_putchar_fd(n % 10 * sign + 48, fd);
}
