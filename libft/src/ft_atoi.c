#include "libft.h"

int		ft_atoi(const char *str)
{
	long	res;
	char	sign;

	res = 0;
	sign = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		++str;
	if (*str == '-' || *str == '+')
		sign = *str++ == '-';
	while (*str >= '0' && *str <= '9')
		res = res * 10 + (*str++ - '0');
	return ((int)(sign ? -res : res));
}
